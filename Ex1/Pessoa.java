package Ex1;
// Classe Pessoa
class Pessoa {
    String nome;
    String sexo;
    int idade;
    boolean vegetariana;
    
    // Construtor
    public Pessoa(String nome, String sexo, int idade, boolean vegetariana) {
        this.nome = nome;
        this.sexo = sexo;
        this.idade = idade;
        this.vegetariana = vegetariana;
    }
}